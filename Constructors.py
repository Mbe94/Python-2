range()
#¿Qué hace?: Genera una secuencia de números, que puede ser usada para iterar en bucles o comprensiones.

#Sintáxis
range(start, stop, step)

#Ejemplo
numeros = [x for x in range(5)]  # [0, 1, 2, 3, 4]

